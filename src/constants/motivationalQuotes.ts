export function getRandomQuote(
  language: 'en' | 'uk' | 'ru' | 'es' | 'fr' | 'de' | 'zh' | 'ja' | 'hi' | 'ar' | 'pt' | 'id' | 'bn' | 'ur' | 'az' | 'tr',
  type: 'warning' | 'encouragement' | 'reminder'
): MotivationalQuote {
  const quotes = MOTIVATIONAL_QUOTES.filter(
    (q) => q.language === language && q.type === type
  );
  return quotes[Math.floor(Math.random() * quotes.length)];
}

export function getWarningQuote(language: 'en' | 'uk' | 'ru' | 'es' | 'fr' | 'de' | 'zh' | 'ja' | 'hi' | 'ar' | 'pt' | 'id' | 'bn' | 'ur' | 'az' | 'tr'): MotivationalQuote {
  return getRandomQuote(language, 'warning');
}

export function getReminderQuote(language: 'en' | 'uk' | 'ru' | 'es' | 'fr' | 'de' | 'zh' | 'ja' | 'hi' | 'ar' | 'pt' | 'id' | 'bn' | 'ur' | 'az' | 'tr'): MotivationalQuote {
  const quotes = MOTIVATIONAL_QUOTES.filter(
    (q) => q.language === language && (q.type === 'encouragement' || q.type === 'reminder')
  );
  return quotes[Math.floor(Math.random() * quotes.length)];
}
